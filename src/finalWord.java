public class finalWord {

    final double phi = 3.14;

    double kelilingLingkaran(double jari) {
        double keliling;
        keliling = 2 * phi * jari;
        return keliling;
    }

    double luasLingkaran(double jari) {
        double luas;
        luas = phi * jari * jari;
        return luas;
    }
}
